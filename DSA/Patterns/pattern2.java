public class pattern2 {
    public static void main(String[] arg){
      int n=4;
      // SQUARE PATTERN
        for(int i=0;i<=n-1;i++){
            for(int j=0;j<=n-1;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
    
}
